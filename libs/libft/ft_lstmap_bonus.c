#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void*), void (*del)(void*))
{
	t_list	*new;
	t_list	*node;
	void	*applied;

	new = NULL;
	while (lst != NULL)
	{
		applied = f(lst->content);
		node = ft_lstnew(applied);
		if (!node)
		{
			del(applied);
			ft_lstclear(&new, del);
			return (NULL);
		}
		ft_lstadd_back(&new, node);
		lst = lst->next;
	}
	return (new);
}
